// ENCODER
void encoder_update_user(uint8_t index, bool clockwise) {
  if (index == 0) {
    if (clockwise) {
      tap_code(KC_VOLD); // Clockwise
    } else {
      tap_code(KC_VOLU); // Counter-Clockwise
    }
  }
}

// RGB
void keyboard_post_init_user(void) {
  // Call the post init code.
  rgblight_enable_noeeprom(); // enables Rgb when keyboard is plugged in
  rgblight_sethsv_noeeprom(255, 0, 30); // sets the color (H,S,B)
}
